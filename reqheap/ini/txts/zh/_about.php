<b>版本 1.0</b>
<br><br><b>ReqHeap</b> 是一个基于Web的需求管理程序。
<br>它允许管理项目、发布版本和需求。
<br><br><b>功能列表</b>:
<ul>
<li>管理项目、子项目、发布版本、测试用例、干系人、术语表
<li>支持不同的用户权限
<li>富文本编辑需求
<li>树形结构
<li>PDF导出
<li>XLS和CSV导出
<li>需求的简洁视图和详细视图
<li>历史记录
<li>多语言支持
<li>每个安装可自定义用户字段
<li>诊断工具
<li>控制面板
<li>统计功能
<li>等等
</ul>
